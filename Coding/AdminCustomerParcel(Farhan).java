    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
          DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); // Clear existing rows
    
    try (Connection connection = DriverManager.getConnection("jdbc:mysql://localhost/adminregisterparcel", "root", "")) {
        String query = "SELECT TrackingNumber, size FROM adminregisterparceldb";
        try (PreparedStatement preparedStatement = connection.prepareStatement(query)) {
            ResultSet resultSet = preparedStatement.executeQuery();
            while (resultSet.next()) {
                String trackingNumber = resultSet.getString("TrackingNumber");
                String size = resultSet.getString("size");
                model.addRow(new Object[]{trackingNumber, size});
            }
        }
    } catch (SQLException e) {
        System.out.println("Error: " + e.getMessage());
     }
    }                                        

